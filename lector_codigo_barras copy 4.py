# Set para almacenar los códigos de barras ya procesados
codigos_procesados = set()

# Diccionario para contar las veces que se ha detectado cada código de barras
contador_codigos = {}

# Función para buscar y mostrar la información
def buscar_persona(numero):
    current_time = datetime.now().strftime("%I:%M %p")  # Obtener la hora actual
    if numero in diccionario:
        informacion = diccionario[numero]
        print(f"{current_time} ⇒ {informacion[0]} → {informacion[1]} → {informacion[4]} → {informacion[3]} → {informacion[2]}")
        print("------------------------------")

        # Agregar el código de barras a los procesados
        codigos_procesados.add(numero)

        return True  # Retorna True indicando que se encontró y mostró la información
    else:
        print(f"{current_time} ⇒ Número de identificación {numero} no encontrado.")
        print("------------------------------")
        return False  # Retorna False indicando que no se encontró la información

# Función para suprimir temporalmente stderr
class suppress_stderr:
    def __enter__(self):
        self.stderr_fd = sys.stderr.fileno()
        self.null_fd = os.open(os.devnull, os.O_RDWR)
        self.saved_stderr_fd = os.dup(self.stderr_fd)
        os.dup2(self.null_fd, self.stderr_fd)
        os.close(self.null_fd)

    def __exit__(self, exc_type, exc_val, exc_tb):
        os.dup2(self.saved_stderr_fd, self.stderr_fd)
        os.close(self.saved_stderr_fd)

# Función para capturar y procesar el código de barras desde la cámara
def escanear_codigo():
    cap = cv2.VideoCapture(0)  # Cambia el índice de la cámara aquí si es necesario

    while True:
        ret, frame = cap.read()

        with suppress_stderr():
            # Decodificar los códigos de barras presentes en el frame
            decoded_objects = decode(frame)

        for obj in decoded_objects:
            barcode_data = obj.data.decode('utf-8')

            # Si el código de barras ya ha sido procesado, continuar
            if barcode_data in codigos_procesados:
                continue

            # Contar la cantidad de veces que se ha detectado este código de barras
            if barcode_data in contador_codigos:
                contador_codigos[barcode_data] += 1
            else:
                contador_codigos[barcode_data] = 1

            # Si se ha detectado el mismo código más de una vez, no hacer nada
            if contador_codigos[barcode_data] > 1:
                continue

            # Convertir el código de barras a cadena y buscar la información
            if buscar_persona(barcode_data):
                # Marcar el código de barras como procesado
                codigos_procesados.add(barcode_data)

        cv2.imshow('Barcode Scanner', frame)
        if cv2.waitKey(1) & 0xFF == ord('q'):
            break

    cap.release()
    cv2.destroyAllWindows()

